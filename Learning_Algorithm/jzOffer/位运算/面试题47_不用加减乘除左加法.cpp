/*
	面试题47：不用加减乘除做加法
	题目：写一个函数，求两个整数之和，要求在函数体内不得使用　+　-　×　÷四则运算符。

	思路：5 + 17 = 22　=>　5 + 7为2不要进位, 10 + 10 = 20；22+2=22
 		     5  00101
 	       + 17 10001
 --------------------
   5^17         10100 不要进位
   (5&17)<<1	00010 进的位
 --------------------
 10100^00010    10110
*/
int add(int a, int b)
{

	int sum, carry;
	do {
		sum = a^b;
		carry = (a&b) << 1;

		a = sum;
		b = carry;
	} while (b != 0);

	return a;
}